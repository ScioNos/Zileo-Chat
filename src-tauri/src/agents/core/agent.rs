/// Task represents a request to the agent
#[derive(Debug, Clone)]
pub struct Task {
    /// Unique task identifier
    pub id: String,
    /// Task description
    pub description: String,
    /// Additional context
    pub context: serde_json::Value,
}

/// Report generated by an agent (Markdown format)
#[derive(Debug, Clone)]
pub struct Report {
    /// Report status
    pub status: ReportStatus,
    /// Full markdown report content (for DB/export)
    pub content: String,
    /// Pure LLM response content only (for display in chat bubble)
    pub response: String,
    /// Execution metrics
    pub metrics: ReportMetrics,
}

/// Report status
#[derive(Debug, Clone)]
pub enum ReportStatus {
    Success,
    Failed,
}

/// Source of a reasoning step
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum ReasoningSource {
    /// Synthetic descriptions from agent flow (e.g. "Analyzing task...", "Calling tool X...")
    AgentFlow,
    /// Real thinking content from reasoning models (e.g. Mistral Magistral)
    ModelThinking,
}

impl std::fmt::Display for ReasoningSource {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            ReasoningSource::AgentFlow => write!(f, "agent_flow"),
            ReasoningSource::ModelThinking => write!(f, "model_thinking"),
        }
    }
}

/// Intermediate reasoning step data for persistence
#[derive(Debug, Clone)]
pub struct ReasoningStepData {
    /// Content of the reasoning step
    pub content: String,
    /// Duration in milliseconds (from start of execution to this step)
    pub duration_ms: u64,
    /// Global ordering sequence within the execution
    pub sequence: u32,
    /// Source of this reasoning step
    pub source: ReasoningSource,
}

/// Detailed tool execution data for persistence
#[derive(Debug, Clone)]
pub struct ToolExecutionData {
    /// Tool type ("local" or "mcp")
    pub tool_type: String,
    /// Tool name (e.g., "MemoryTool", "find_symbol")
    pub tool_name: String,
    /// MCP server name (only for MCP tools)
    pub server_name: Option<String>,
    /// Input parameters as JSON
    pub input_params: serde_json::Value,
    /// Output result as JSON
    pub output_result: serde_json::Value,
    /// Whether execution was successful
    pub success: bool,
    /// Error message if failed
    pub error_message: Option<String>,
    /// Duration in milliseconds
    pub duration_ms: u64,
    /// Iteration number in the tool loop
    pub iteration: u32,
    /// Global ordering sequence within the execution
    pub sequence: u32,
}

/// Metrics collected during task execution
#[derive(Debug, Clone)]
pub struct ReportMetrics {
    /// Duration in milliseconds
    pub duration_ms: u64,
    /// Cumulative input tokens billed across all LLM API calls (for cost calculation)
    pub tokens_input: usize,
    /// Output tokens generated (cumulative across all iterations)
    pub tokens_output: usize,
    /// Last API call's input tokens (context window size for display)
    pub context_tokens: usize,
    /// Cached input tokens - cache reads (if provider supports prompt caching)
    pub cached_tokens: Option<usize>,
    /// Cache-write tokens (if provider supports prompt caching)
    pub cache_write_tokens: Option<usize>,
    /// Thinking/reasoning tokens (if reasoning model used)
    pub thinking_tokens: Option<usize>,
    /// Tools used (names only, for backward compatibility)
    pub tools_used: Vec<String>,
    /// MCP calls made (names only, for backward compatibility)
    pub mcp_calls: Vec<String>,
    /// Detailed tool execution data for persistence
    pub tool_executions: Vec<ToolExecutionData>,
    /// Intermediate reasoning steps collected during execution
    pub reasoning_steps: Vec<ReasoningStepData>,
    /// Per-iteration token breakdown (one entry per LLM API call in tool loop)
    pub iteration_metrics: Vec<IterationMetrics>,
}

impl ReportMetrics {
    /// Creates a ReportMetrics with all fields zeroed/empty.
    ///
    /// Use this for early-return error reports where no LLM call was made.
    /// Accepts `duration_ms` since even failed reports track elapsed time.
    pub fn empty(duration_ms: u64) -> Self {
        Self {
            duration_ms,
            tokens_input: 0,
            tokens_output: 0,
            context_tokens: 0,
            cached_tokens: None,
            cache_write_tokens: None,
            thinking_tokens: None,
            tools_used: vec![],
            mcp_calls: vec![],
            tool_executions: vec![],
            reasoning_steps: vec![],
            iteration_metrics: vec![],
        }
    }
}

impl Report {
    /// Creates a failed report with a formatted error message.
    ///
    /// Provides consistent formatting for error reports across the agent.
    ///
    /// # Arguments
    /// * `agent_id` - The agent identifier (for the report header)
    /// * `task_description` - Description of the task that failed
    /// * `error_msg` - The error message to include
    /// * `duration_ms` - Elapsed time in milliseconds
    pub fn failed(
        agent_id: &str,
        task_description: &str,
        error_msg: String,
        duration_ms: u64,
    ) -> Self {
        Self {
            status: ReportStatus::Failed,
            content: format!(
                "# Agent Report: {}\n\n**Task**: {}\n\n**Status**: Failed\n\n## Error\n{}",
                agent_id, task_description, error_msg
            ),
            response: error_msg,
            metrics: ReportMetrics::empty(duration_ms),
        }
    }

    /// Creates a failed report with partial metrics (from a tool execution loop).
    ///
    /// Use this when an LLM call fails mid-execution and some token/tool metrics
    /// have already been accumulated.
    ///
    /// # Arguments
    /// * `agent_id` - The agent identifier (for the report header)
    /// * `task_description` - Description of the task that failed
    /// * `error_msg` - The error message to include
    /// * `metrics` - Accumulated metrics from partial execution
    pub fn failed_with_metrics(
        agent_id: &str,
        task_description: &str,
        error_msg: String,
        metrics: ReportMetrics,
    ) -> Self {
        Self {
            status: ReportStatus::Failed,
            content: format!(
                "# Agent Report: {}\n\n**Task**: {}\n\n**Status**: Failed\n\n## Error\n\n{}",
                agent_id, task_description, error_msg
            ),
            response: error_msg,
            metrics,
        }
    }
}

/// Agent trait - unified interface for all agents
#[async_trait]
// Trait used via dyn dispatch in agent registry; methods called from lib code only.
#[allow(dead_code)]
pub trait Agent: Send + Sync {
    /// Executes a task and returns a report
    ///
    /// This is the legacy method without MCP support.
    /// New implementations should prefer `execute_with_mcp`.
    async fn execute(&self, task: Task) -> anyhow::Result<Report>;

    /// Executes a task with access to MCP tools and optional cancellation support.
    ///
    /// This method allows the agent to call MCP tools during execution.
    /// The default implementation delegates to `execute()` for backward compatibility.
    ///
    /// # Arguments
    /// * `task` - The task to execute
    /// * `mcp_manager` - Optional MCP manager for tool invocation
    /// * `cancellation_token` - Optional cancellation token for graceful shutdown.
    ///   When provided, the agent should propagate this token to sub-agent tools
    ///   so they can abort when the user cancels the workflow.
    ///
    /// # Returns
    /// A report containing the execution results and metrics
    async fn execute_with_mcp(
        &self,
        task: Task,
        _mcp_manager: Option<Arc<MCPManager>>,
        _cancellation_token: Option<CancellationToken>,
    ) -> anyhow::Result<Report> {
        // Default implementation for backward compatibility
        self.execute(task).await
    }

    /// Returns agent capabilities
    fn capabilities(&self) -> Vec<String>;

    /// Returns lifecycle type
    fn lifecycle(&self) -> Lifecycle;

    /// Returns available tools
    fn tools(&self) -> Vec<String>;

    /// Returns required MCP servers
    fn mcp_servers(&self) -> Vec<String>;

    /// Returns system prompt
    fn system_prompt(&self) -> String;

    /// Returns agent configuration
    fn config(&self) -> &AgentConfig;
}
